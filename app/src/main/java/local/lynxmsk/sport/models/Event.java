package local.lynxmsk.sport.models;

public class Event {

    private String title;
    private String time;
    private String place;
    private String article;

    public String getTitle() {
        return title;
    }

    public String getTime() {
        return time;
    }

    public String getPlace() {
        return place;
    }

    public String getArticle() {
        return article;
    }
}
